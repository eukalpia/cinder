import 'dart:async';

import 'package:cinder/cinder.dart';

/// Animated isometric terminal city used by the Cinder documentation homepage.
void main() {
  runApp(const CinderApp(child: WebShowcase()));
}

class WebShowcase extends StatefulWidget {
  const WebShowcase({super.key});

  @override
  State<WebShowcase> createState() => _WebShowcaseState();
}

class _WebShowcaseState extends State<WebShowcase> {
  Timer? _timer;
  int _frame = 0;
  bool _showPipeline = true;

  @override
  void initState() {
    super.initState();
    _timer = Timer.periodic(const Duration(milliseconds: 420), (_) {
      if (!mounted) return;
      setState(() => _frame = (_frame + 1) % _cityFrames.length);
    });
  }

  @override
  Widget build(BuildContext context) {
    return Focus(
      autofocus: true,
      onKeyEvent: (event) {
        if (event.logicalKey == LogicalKey.space) {
          setState(() => _showPipeline = !_showPipeline);
          return true;
        }
        return false;
      },
      child: Container(
        color: const Color.fromRGB(5, 7, 11),
        padding: const EdgeInsets.symmetric(horizontal: 1),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Expanded(
              child: Center(
                child: Text(
                  _cityFrames[_frame],
                  style: const TextStyle(
                    color: Colors.magenta,
                    fontWeight: FontWeight.bold,
                  ),
                ),
              ),
            ),
            Row(
              mainAxisAlignment: MainAxisAlignment.spaceBetween,
              children: [
                Text(
                  _showPipeline
                      ? ' WIDGET → ELEMENT → RENDER OBJECT → BUFFER → DIFF '
                      : ' SPACE: toggle pipeline ',
                  style: const TextStyle(color: Colors.gray),
                ),
                Text(
                  'FRAME ${(_frame + 1).toString().padLeft(2, '0')}  ',
                  style: const TextStyle(color: Colors.green),
                ),
              ],
            ),
          ],
        ),
      ),
    );
  }

  @override
  void dispose() {
    _timer?.cancel();
    super.dispose();
  }
}

